				<h2>Dalam Sehari Level PPKM Jabodetabek Berubah, Ini Penjelasan Satgas Covid-19</h2>
				<ul class="info-list">
					<li>07 Juli ,2022</li>
					<li>Berita Terkini</li>
				</ul>
                <div class="image">
                    <img src="assets/images/background/berita-3-detail.png" alt="" />
                </div>
				<div class="big-text"><strong>JAKARTA, KOMPAS.com</strong> - Satgas Covid-19 buka suara terkait berubahnya level asesmen Pemberlakuan Pembatasan Kegiatan Masyarakat (PPKM) di  ke level 1 sejak Rabu (6/7/2022), setelah sehari sebelumnya dikategorikan level 2.</div>
				<p>Kepala Bidang Penanganan Kesehatan Satgas Covid-19, Alexander K. Ginting mengatakan, berubahnya level asesmen PPKM di Jabodetabek selang sehari itu disebabkan karena adanya perubahan cara pandang levelisasi.</p>
                <p>Selama ini, kata dia, levelisasi mengacu pada Instruksi Dalam Negeri (Inmendagri) Nomor 29 Tahun 2022.</p>
                <p>Jika mengacu pada beleid tersebut, seharusnya Jabodetabek masuk kriteria PPKM level 1 sejak awal.</p>
                <p>"Dari awal sebenarnya tetap sesuai PPKM level 1 jika kita konsisten kriterianya seperti Inmendagri Nomor 29. Yang terjadi ada perbedaan cara pandang dalam menetapkan levelisasi yang selama ini mengacu Inmendagri Nomor 29," ucap Alexander saat dihubungi Kompas.com, Kamis (7/7/2022).</p>
                <p>Alexander menjelaskan, dalam Inmendagri Nomor 29, penentuan levelisasi PPKM berdasarkan tiga hal, yakni indikator laju penularan, indikator kapasitas respon, dan capaian vaksinasi. </p>
                <p>Indikator laju penularan meliputi kasus konfirmasi, perawatan rumah sakit, dan angka kematian di mana transmisi komunitas dihitung per 100.000 penduduk.</p>
                <p>Sementara itu, indikator kapasitas respons dilihat dari testing/angka kasus positif, kontak erat, dan tingkat keterisian tempat tidur (<em>Bed Ocupancy Ratio</em>/BOR) Rumah Sakit (RS).</p>
                <p>Kemudian, capaian vaksinasi dilihat dari akselerasi vaksin dasar lengkap (dosis 1 dan dosis 2) sebesar 81.06 persen dan vaksinasi lengkap lansia 67,5 persen.</p>
                <p>Di Jabodetabek kata Alexander, hanya angka penularan dan kasus konfirmasi yang meningkat, sedangkan BOR, kasus positif, dan kasus kematian masih rendah.</p>
                <p>"Di Jabodetabek ternyata yang tinggi hanya penularan dan kasus konfirmasi karena varian baru BA.4 dan BA.5. Jadi PPKM level 2 Jabodetabek direvisi," jelasnya rinci.</p>
                <p>Menurutnya, asesmen level PPKM Jabodetabek tidak bisa ditingkatkan ke level 2 jika hanya karena kasus konfirmasi harian di atas 1000 per hari.</p>
                <p>PPKM level 2 Jabodetabek direvisi karena kendati penularan naik, indikator lainnya masih terkendali. Positivity rate di angka 5,2 persen, BOR nasional 2,5 persen, dan kematian 2,58 persen. Apalagi kata dia, Omicron BA.4 dan BA.5 ini tidak sama dengan varian Delta yang menular lebih cepat.</p>
                <p>"Memang benar sudah 80 persen lebih kasusnya Omicron BA.4 dan BA.5 yang penularannya cepat, tapi tidak se-virulen Delta, sehingga gambaran awal Juli 2021 dikala kita PPKM darurat tidaklah sama dengan Juli 2022 yang sesungguhnya masih PPKM level 1," sebut Alexander.</p>
                <p>Sebagai informasi, pemerintah mengubah level PPKM di Jabodetabek sehari setelah ditetapkan.</p>
                <p>Perubahan level PPKM secara singkat ini, semuanya berdasarkan pada dua Instruksi Menteri Dalam Negeri (Inmendagri).</p>
                <p>Pada mulanya, Inmendagri Nomor 33 Tahun 2022 terbit dan mulai diberlakukan 5 Juli -1 Agustus 2022.</p>
                <p>Di dalam aturan itu, seluruh kota dan kabupaten administratif yang ada di DKI Jakarta masuk dalam level 2 PPKM.</p>
                <p>Tetapi, pada Rabu (6/7/2022), aturan terbaru kembali diterbitkan dan diberlakukan, yakni Inmendagri Nomor 35 Tahun 2022.</p>
                <p>Dalam aturan baru ini, seluruh wilayah di DKI Jakarta ditetapkan ada pada level 1 PPKM.</p>

