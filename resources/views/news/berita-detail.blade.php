                <div class="image">
                    <img src="assets/images/background/detail-1.jpg" alt="" />
                </div>
				<div class="big-text"><strong>TEMPO.CO, Jakarta</strong> - Kepala Badan Nasional Penanggulangan Bencana (BNPB) selaku Ketua Satgas Covid-19 Letjen TNI Suharyanto mengeluarkan syarat terbaru perjalanan orang dalam negeri.</div>
				<p>“Dalam rangka menindaklanjuti dinamika situasi persebaran virus SARS-CoV-2 serta upaya pemulihan ekonomi nasional,” kata Suharyanto soal alasan syarat perjalanan itu, dalam keterangan tertulisnya, Sabtu, 9 Juli 2022.</p>
                <p>Pelaku Perjalanan Dalam Negeri (PPDN) wajib menggunakan aplikasi PeduliLindungi dalam melakukan perjalanan dalam negeri.</p>
                <p>PPDN dengan moda transportasi udara, laut, darat menggunakan kendaraan pribadi atau umum, penyeberangan, dan kereta api antarkota dari dan ke daerah di seluruh Indonesia yang mendapatkan vaksinasi dosis ketiga (booster), tidak wajib menunjukan hasil negatif tes RT-PCR atau rapid test antigen.</p>
                <p>PPDN yang mendapatkan vaksinasi dosis kedua wajib menunjukkan hasil negatif rapid test antigen yang sampelnya diambil dalam kurun waktu 1 x 24 jam, atau hasil negatif tes RT-PCR yang sampelnya diambil dalam kurun waktu 3 x 24 jam sebelum keberangkatan sebagai syarat perjalanan. PPDN dapat melakukan vaksinasi dosis ketiga (booster) on-site saat keberangkatan;</p>
                <p>PPDN yang mendapatkan vaksinasi dosis pertama wajib menunjukkan hasil negatif tes RT-PCR yang sampelnya diambil dalam kurun waktu 3 x 24 jam sebelum keberangkatan sebagai syarat perjalanan.</p>
                <div class="image">
                    <img src="assets/images/background/berita-detail-plus-1.jpeg" alt="" />
                </div>
                <p>PPDN dengan kondisi kesehatan khusus atau penyakit komorbid yang menyebabkan tidak dapat menerima vaksinasi dikecualikan terhadap ketentuan vaksinasi. Namun, mereka wajib menunjukkan hasil negatif tes RTPCR yang sampelnya diambil dalam kurun waktu 3 x 24 jam sebelum keberangkatan dan wajib melampirkan surat keterangan dokter dari Rumah Sakit Pemerintah yang menyatakan bahwa yang bersangkutan belum dan/atau tidak dapat mengikuti vaksinasi Covid-19;</p>
                <p>PPDN dengan usia 6-17 tahun wajib menunjukkan kartu/sertifikat vaksin dosis kedua tanpa menunjukan hasil negatif tes RT-PCR atau rapid test antigen.</p>
                <p>PPDN dengan usia dibawah 6 tahun dikecualikan dari ketentuan vaksinasi dan tidak wajib menunjukan hasil negatif tes RT-PCR atau rapid test antigen. Namun, wajib melakukan perjalanan dengan pendamping yang telah memenuhi ketentuan vaksinasi dan pemeriksaan Covid-19 serta menerapkan protokol kesehatan secara ketat.</p>
