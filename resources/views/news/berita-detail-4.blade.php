				<h2>Jokowi: Pandemi Covid-19 Sudah Melandai, Aktivitas Masyarakat Mulai Normal</h2>
				<ul class="info-list">
					<li>07 Juli ,2022</li>
					<li>Berita Terkini</li>
				</ul>
                <div class="image">
                    <img src="assets/images/background/berita-4-detail.png" alt="" />
                </div>
				<div class="big-text"><strong>JAKARTA, KOMPAS.com</strong> - Presiden Joko Widodo mengatakan, situasi pandemi Covid-19 mulai menunjukkan perbaikan.</div>
				<p>Menurut dia, kondisi ini merupakan keberhasilan dari kerja keras semua pihak dalam menghadapi pandemi.</p>
                <p>"Alhamdulillah saat ini kondisi pandemi Covid-19 sudah melandai. Aktivitas masyarakat sudah mulai kembali normal, ekonomi masyarakat sudah mulai bergerak kembali," kata Jokowi dalam tayangan YouTube Sekretariat Presiden, Senin (30/5/2022).</p>
                <p>Kendati demikian, Jokowi mengingatkan semua pihak untuk tidak lengah. Dia mengatakan, situasi yang baik ini harus terus dijaga.</p>
                <p>Oleh karenanya, presiden meminta masyarakat tetap melakukan vaksinasi Covid-19 lengkap atau dua dosis, ditambah vaksinasi <em>booster</em> atau dosis ketiga. Ini untuk memastikan pencegahan penularan virus corona.</p>
                <p>Jokowi mengatakan, vaksinasi <em>booster</em> sangat penting. Data dari Kementerian Kesehatan menunjukkan, vaksinasi <em>booster</em>  dapat meningkatkan kekebalan imunitas hingga dua kali lipat dibanding dosis kedua.</p>
                <p>"Vaksinasi <em>booster</em> ini juga diperlukan untuk melindungi orangtua dan kelompok masyarakat rentan atau memiliki komorbid dari penularan Covid-19," ucapnya.</p>
                <p>Jokowi memastikan, stok vaksin Covid-19 pemerintah untuk <em>booster</em> lebih dari cukup.</p>
                <p>Dia meminta masyarakat segera mendapatkan vaksinasi <em>booster</em> gratis dan tidak memilih-milih jenis vaksin. Sebab, semua vaksin manfaatnya sama untuk melindungi masyarakat dari pandemi Covid-19.</p>
                <p>"Mari kita jaga bersama-sama momentum baik ini agar Indonesia semakin pulih dan ekonomi semakin membaik," kata kepala negara.</p>
                <p>Sebagaimana diketahui, kasus Covid-19 menunjukkan penurunan beberapa waktu terakhir. Data terbaru Satuan Tugas Penanganan Covid-19 pada Senin (30/5/2022) mencatat, bertambah 218 kasus Covid-19.</p>
                <p>Dengan demikian, total ada 6.054.633 kasus virus corona terhitung sejak awal pandemi, 2 Maret 2020.</p>
                <p>Data yang sama memperlihatkan, bertambah 12 orang meninggal dunia. Sementara, pasien sembuh bertambah 287 orang.</p>
                <p>Penambahan kasus tersebut menyebabkan jumlah kasus aktif Covid-19 saat ini sebesar 2.871 kasus.</p>
                <p>Adapun vaksinasi dosis pertama telah menjangkau 200.202.182 orang. Lalu, vaksinasi dosis kedua telah diterima 167.330.132 orang.</p>
                <p>Kemudian, warga yang sudah mendapat vaksinasi dosis ketiga atau <em>booster</em> sebanyak 45.412.157 orang.</p>

