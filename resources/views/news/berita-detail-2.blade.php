				<h2>3 Kabar Baik Corona di Indonesia Bikin Ragam Aturan Lebih Longgar</h2>
				<ul class="info-list">
					<li>08 Maret ,2022</li>
					<li>Berita</li>
				</ul>
                <div class="image">
                    <img src="assets/images/background/beriat-2-detail.jpg" alt="" />
                </div>
				<div class="big-text"><strong>Jakarta</strong> - Kondisi terkini kasus COVID-19 di Indonesia terus mengalami penurunan. Hal itu membuat sejumlah aturan terkait Corona menjadi lebih longgar.</div>
				<p>Kabar baik soal COVID-19 di Tanah Air ini disampaikan oleh pemerintah pada Senin (7/3/2022). Pemerintah menyebut kasus harian Corona turun sangat signifikan.</p>
                <p>"Pemerintah memastikan kondisi dan penanganan pandemi terus membaik. Berdasarkan data yang kami evaluasi, tren kasus harian nasional menurun sangat signifikan," kata Menko Kemaritiman dan Investasi Luhut Binsar Pandjaitan dalam jumpa pers virtual, kemarin.</p>
                <p>Luhut juga menyampaikan kabar baik terkait kondisi rawat inap rumah sakit mengalami penurunan. Kasus kematian juga terus melandai.</p>
                <p>Penurunan ini disebut Luhut terjadi hampir di seluruh provinsi di Jawa-Bali. Hanya Yogyakarta yang kasusnya disebut masih belum menurun.</p>
                <p>"Namun DIY kami perkirakan akan turun dalam beberapa hari ke depan ini," ujar Luhut.</p>
                <p>Lalu aturan apa saja yang dilonggarkan karena Corona di Indonesia telah menurun? Berikut ini rinciannya:</p>
                <h3>Jabodetabek-Surabaya Raya PPKM Level 2</h3>
                <p>Luhut menyatakan situasi pandemi Corona mulai membaik. Sejumlah kabupaten/kota juga masuk lagi ke PPKM level 2, termasuk wilayah aglomerasi Jabodetabek.</p>
                <p>"Saat ini aglomerasi Jabodetabek dan Surabaya Raya kembali masuk ke level 2 karena penurunan kasus konfirmasi harian dan juga rawat inap rumah sakit," ucapnya.</p>
                <p>Wilayah Jabodetabek sendiri sempat berstatus PPKM level 3 akibat kenaikan jumlah kasus harian Corona.</p>
                <h3>Naik Pesawat-KA Tak Wajib Tes Corona</h3>
                <p>Pelaku perjalanan domestik yang sudah mendapat dosis vaksinasi lengkap tidak perlu lagi menunjukkan bukti tes antigen dan PCR negatif. Aturan ini akan dituangkan dalam surat edaran.</p>
                <p>"Pelaku perjalanan domestik dengan transportasi udara, laut maupun darat yang sudah melakukan vaksinasi dosis kedua dan lengkap, sudah tidak perlu menunjukkan bukti tes antigen dan PCR negatif," kata Luhut.</p>
                <p>Luhut mengatakan bahwa aturan baru ini akan ditetapkan dalam surat edaran. Surat edaran ini akan segera diterbitkan dalam waktu dekat.</p>
                <h3>Karantina Dikurangi Jadi 1 Hari untuk Umrah dan PPLN</h3>
                <p>Menko Perekonomian Airlangga Hartarto mengatakan Presiden Joko Widodo (Jokowi) memerintahkan agar durasi karantina jemaah umroh dan pelaku perjalanan luar negeri (PPLN) dikurangi. Dia menyebut karantina bakal dilakukan 1 hari bagi jemaah umroh dan PPLN.</p>
                <p>"Kasus umrah itu yang pulang dari umrah positif rata-rata sebesar 47% in dan out. Tadi arahan Bapak Presiden bahwa karantina sudah dikurangi menjadi 1 hari baik itu umroh maupun PPLN," ucap Airlangga dalam konferensi pers virtual, Senin (7/3).</p>
                <p>Aturan itu bakal mulai berlaku hari ini. Aturan lengkap bakal disampaikan lewat surat edaran Satgas COVID-19.</p>
                <p>Dia mengatakan orang yang tiba di Indonesia dari luar negeri akan langsung diisolasi jika positif COVID-19 saat dites.</p>

